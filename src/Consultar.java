    public void descargarUno()
    {
        String nombreDoc="";
        try{
            nombreDoc= tb_documentos.getValueAt(tb_documentos.getSelectedRow(), 1).toString();
        }catch(Exception e)
        {
            System.err.println(e.getMessage());
            
        }
        
         
        if(nombreDoc.equalsIgnoreCase(""))
        {
            cuadroDialogo("Seleccione documento");
        }
        else
        {
            System.out.println(nombreDoc);
        
        JFileChooser fc=new JFileChooser();
        fc.setDialogTitle("Selecciona Carpeta para Descarga"); 
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); 
        int seleccion=fc.showOpenDialog(this);
        if(seleccion==JFileChooser.APPROVE_OPTION){
            //Seleccionamos el fichero
            System.out.println(fc.getSelectedFile().toString());         
            apiclientes.downloadDoc(nombreDoc, fc.getSelectedFile().toString());
            //System.out.println(fichero.getName());
            //Ecribe la ruta del fichero seleccionado en el campo de texto  
           
        }
        }
        
    }
