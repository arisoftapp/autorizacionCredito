public void limpiarCampos()
    {
        txt_nombre.setText("");
        txt_paterno.setText("");
        txt_materno.setText("");
        txt_puesto.setText("");
        txt_comentarios.setText("");
        DefaultTableModel modelo = (DefaultTableModel)tb_autorizados.getModel();
        if(modelo.getRowCount()>0)
        {
            modelo.getDataVector().removeAllElements();
            modelo.fireTableDataChanged();
        }
        DefaultTableModel modelo2 = (DefaultTableModel)tb_documentos.getModel();
        if(modelo2.getRowCount()>0)
        {
            modelo2.getDataVector().removeAllElements();
            modelo2.fireTableDataChanged();
        }
        
    }
